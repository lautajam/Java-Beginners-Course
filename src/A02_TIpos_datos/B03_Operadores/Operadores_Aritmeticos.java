package A02_TIpos_datos.B03_Operadores;

public class Operadores_Aritmeticos {
    public static void main(String[] args) {
        int num_1, num_2, resultado;

        num_1 = 4;
        num_2 = 3;
        resultado = num_1 + num_2;
        System.out.println("El resultado (+) es: " + resultado);

        resultado = num_1 - num_2;
        System.out.println("El resultado (-) es: " + resultado);

        resultado = num_1 * num_2;
        System.out.println("El resultado (*) es: " + resultado);

        num_1 = 8;
        num_2 = 2;
        resultado = num_1 / num_2;
        System.out.println("El resultado (/) es: " + resultado);

    }
}
