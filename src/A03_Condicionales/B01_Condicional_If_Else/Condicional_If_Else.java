package A03_Condicionales.B01_Condicional_If_Else;

public class Condicional_If_Else {
    public static void main(String[] args) {

        int num = 7, num2 = 5;

        if (num > num2) {
            System.out.println("El número " + num + " es mayor que " + num2);
        } else {
            System.out.println("El número " + num + " es menor que " + num2);
        }

    }
}
